////////////////////////////////////////////////////////////////////////////////
// Use the row-major as prefered way to index the thread array
////////////////////////////////////////////////////////////////////////////////
#define ROW_MAJOR_IDX
////////////////////////////////////////////////////////////////////////////////
// NAIVE PROCESSING CONSTANTS :
// Despite every workers performs an independant task, ie computing a row-col
// dot-product, we group the threads to optimize the payload distribution
// between the Compute Units. The size of a square group is specified by the
// following define.
////////////////////////////////////////////////////////////////////////////////
#define WGS      32
////////////////////////////////////////////////////////////////////////////////
// SMB PROCESSING CONSTANTS :
// Each work-group is in-charge of computing a tile of the dimension TSxTS
// in the output matrix.
//
// Note that TS must be smaller than the SQRT of MAX(WorkGroupSize),
// which equals to 1024 on the development device (a NVIDIA GTX 1060).
////////////////////////////////////////////////////////////////////////////////
#define SMB_TS   32
////////////////////////////////////////////////////////////////////////////////
// CRB PROCESSING CONSTANTS :
// The product between tiles are themselves computed by block, in order to
// optimize the memory schedules.
//
// The size of the square sub-tiles employed in these products is specified
// by the STS define.
////////////////////////////////////////////////////////////////////////////////
#define CRB_TS   32
#define CRB_STS   4
